//! Canonical CSS property names.

macro_rules! property_names {
    ($($variant:ident => $css_name:literal),+ $(,)?) => {
        /// A non-exhaustive catalog of canonical CSS property names.
        ///
        /// Each variant maps to its kebab-case CSS spelling through [`PropertyName::as_str`].
        /// The catalog is deliberately independent from the narrower checked-selector surface.
        #[allow(missing_docs)]
        #[derive(Clone, Copy, Debug, PartialEq, Eq, Hash)]
        #[non_exhaustive]
        pub enum PropertyName {
            $($variant),+
        }

        impl PropertyName {
            /// Return the canonical kebab-case CSS spelling.
            #[must_use]
            pub const fn as_str(&self) -> &'static str {
                match self {
                    $(Self::$variant => $css_name),+
                }
            }
        }
    };
}

